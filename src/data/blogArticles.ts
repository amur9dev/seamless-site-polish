/**
 * Функция поиска статьи по ID
 */
export const getArticleById = (id: string): BlogArticle | undefined => {
  return BLOG_ARTICLES.find(article => article.id === id);
};

/**
 * Функция получения похожих статей (исключая текущую)
 */
export const getRelatedArticles = (currentId: string, limit: number = 3): BlogArticle[] => {
  return BLOG_ARTICLES
    .filter(article => article.id !== currentId)
    .slice(0, limit);
};

/**
 * Форматирование даты
 */
export const formatDate = (dateString: string) => {
  const date = new Date(dateString);
  return date.toLocaleDateString('ru-RU', {
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });
};
